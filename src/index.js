function decode(expr) {
    // write your solution here
   return expr.match(/.{1,10}/g).map((item)=>{
    if(item === '**********'){
        return ' '
    }

    const morseCode = item.replace(/10/g,'.').replace(/11/g,'-').replace(/00/gm,'')
    return  MORSE_TABLE[morseCode]

   }).join('')

}

